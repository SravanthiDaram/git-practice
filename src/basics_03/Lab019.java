package basics_03;

public class Lab019 {
    public static void main(String[] args) {
        // Compound assignment operators
        // +=, -=, %=, *=

        int a = 25;
        int b = 35;
        int d = 5;
        a += 26; // a = a+26
        b -= 10; // b= b-10
        System.out.println(a);
        System.out.println(b);

        d *= 6;
        System.out.println(d);

    }
    
}
